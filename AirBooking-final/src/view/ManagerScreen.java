    protected void initialFlights() {
        this.jList1.removeAll();
        Connection con = DatabaseModel.getInstance().getConnection();
        try {
            String temp = "";
            Statement stmt = con.createStatement(ResultSet.CONCUR_UPDATABLE, ResultSet.CONCUR_UPDATABLE);
            ResultSet result = stmt.executeQuery("SELECT id FROM tickets");
            result.first();
            do{
                temp += result.getString("id") + " ";
            } while (result.next());
            String[] list = temp.split(" ");
            this.jList1.setListData(list);
        } catch (Exception exp)
        {
        }
    }

    public FlightModel getFlightModel() {
        FlightModel flight = new FlightModel();
        flight.setID(this.jTextField10.getText());
        flight.setDeparture(this.jTextField1.getText());
        flight.setDestination(this.jTextField2.getText());
        flight.setDate(this.jTextField3.getText());
        flight.setFirst(Double.parseDouble(this.jTextField4.getText()));
        flight.setBusiness(Double.parseDouble(this.jTextField5.getText()));
        flight.setEconomy(Double.parseDouble(this.jTextField6.getText()));
        flight.setPromotion(Integer.parseInt(this.jTextField8.getText()));
        flight.setDiscount(Double.parseDouble(this.jTextField9.getText()));
        return flight;
    }

