    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        Connection con = DatabaseModel.getInstance().getConnection();
        try {
            Statement stmt = con.createStatement(ResultSet.CONCUR_UPDATABLE, ResultSet.CONCUR_UPDATABLE);
            ResultSet result = stmt.executeQuery("SELECT * FROM tickets WHERE id = '" + this.jList1.getSelectedValue().toString() + "'");
            result.first();
            do{
                this.jTextField10.setText(result.getString("id"));
                this.jTextField1.setText(result.getString("departure"));
                this.jTextField2.setText(result.getString("destination"));
                this.jTextField3.setText(result.getString("date"));
                this.jTextField4.setText(result.getString("firstClassFee"));
                this.jTextField5.setText(result.getString("bizClassFee"));
                this.jTextField6.setText(result.getString("econClassFee"));
                this.jTextField8.setText(result.getString("promotion"));
                this.jTextField9.setText(result.getString("discount"));
            } while (result.next());

        } catch (Exception exp)
        {
        }
    }//GEN-LAST:event_jList1ValueChanged

