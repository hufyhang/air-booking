    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        Connection con = DatabaseModel.getInstance().getConnection();
        try {
            Statement stmt = con.createStatement(ResultSet.CONCUR_UPDATABLE, ResultSet.CONCUR_UPDATABLE);
            ResultSet result = stmt.executeQuery("SELECT * FROM users WHERE username = '" + this.jComboBox1.getSelectedItem().toString() + "'");
            result.first();
            do{
                this.jTextField1.setText(result.getString("username"));
                this.jTextField2.setText(result.getString("password"));
                this.jTextField3.setText(result.getString("email"));
            } while (result.next());

        } catch (Exception exp)
        {
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

