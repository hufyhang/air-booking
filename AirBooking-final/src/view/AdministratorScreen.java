    protected void initialManagers() {
        this.jComboBox1.removeAllItems();
        Connection con = DatabaseModel.getInstance().getConnection();
        try {
            Statement stmt = con.createStatement(ResultSet.CONCUR_UPDATABLE, ResultSet.CONCUR_UPDATABLE);
            ResultSet result = stmt.executeQuery("SELECT username FROM users WHERE role = 'manager'");
            result.first();
            do{
                this.jComboBox1.addItem(result.getString("username"));
            } while (result.next());

        } catch (Exception exp)
        {
        }
    }

